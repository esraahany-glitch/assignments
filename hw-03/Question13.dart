/*Write a Dart program that calculates
 a grade (A, B, C, or D) based on a mark. Then use a switch
statement to print a message for each grade.*/



void main() {
  String grade = 'a';

  switch (grade) {
    case ' b':
      print('your grade is b ');
      break;
    case 'c':
      print('your grade is c ');
      break;
    case 'd':
      print('your grade is d ');
      break;
    case ' a ':
      print('your grade is a ');
      break;
  }
}
